/**
 * Check if a test is scheduled for a future time
 * परीक्षा अगर भविष्य के लिए शेड्यूल है तो चेक करता है
 * 
 * @param {string} testId - Test ID (e.g., 'test1', 'test2')
 * @returns {boolean} - true if test is scheduled for future, false if available now
 */
function isTestScheduled(testId) {
  if (!TESTS[testId] || !TESTS[testId].scheduledTime) {
    return false;
  }

  const scheduledTime = new Date(TESTS[testId].scheduledTime);
  const currentTime = new Date();

  return scheduledTime > currentTime;
}

/**
 * Get scheduled time for a test in readable format (Hindi)
 * परीक्षा का शेड्यूल समय पढ़ने योग्य फॉर्मेट में मिलता है
 * 
 * @param {string} testId - Test ID
 * @returns {string} - Formatted time string or empty if not scheduled
 * Example: "18 अप्रैल, 2026 को रात 11:15 बजे शुरू होगी।"
 */
function getScheduledTimeText(testId) {
  if (!TESTS[testId] || !TESTS[testId].scheduledTime) {
    return '';
  }

  const scheduledTime = new Date(TESTS[testId].scheduledTime);
  const currentTime = new Date();

  if (scheduledTime <= currentTime) {
    return '';
  }

  const months = [
    'जनवरी', 'फरवरी', 'मार्च', 'अप्रैल', 'मई', 'जून',
    'जुलाई', 'अगस्त', 'सितंबर', 'अक्टूबर', 'नवंबर', 'दिसंबर'
  ];

  const day = scheduledTime.getDate();
  const month = months[scheduledTime.getMonth()];
  const year = scheduledTime.getFullYear();
  let hours = scheduledTime.getHours();
  const minutes = String(scheduledTime.getMinutes()).padStart(2, '0');

  const ampm = hours >= 12 ? 'रात' : 'सुबह';
  if (hours > 12) hours -= 12;
  if (hours === 0) hours = 12;

  return `${day} ${month}, ${year} को ${ampm} ${hours}:${minutes} बजे शुरू होगी।`;
}

/**
 * Get minutes remaining until test is available
 * परीक्षा उपलब्ध होने तक बाकी मिनट
 * 
 * @param {string} testId - Test ID
 * @returns {number} - Minutes remaining, or 0 if available now
 */
function getMinutesUntilTestAvailable(testId) {
  if (!TESTS[testId] || !TESTS[testId].scheduledTime) {
    return 0;
  }

  const scheduledTime = new Date(TESTS[testId].scheduledTime);
  const currentTime = new Date();
  const diffMs = scheduledTime - currentTime;

  if (diffMs <= 0) return 0;

  return Math.ceil(diffMs / (1000 * 60));
}

